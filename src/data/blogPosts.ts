const parseDate = (value: string): number => {
  const parsed = Date.parse(value);
  return Number.isNaN(parsed) ? 0 : parsed;
};

const normalizeTitle = (value: string): string => {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
};

const buildBlogPosts = (): BlogPost[] => {
  const slugSet = new Set<string>();
  const aliasSet = new Set<string>();
  const titleSet = new Set<string>();
  const merged: BlogPost[] = [];

  const tryAddPost = (post: BlogPost) => {
    const normalizedTitle = normalizeTitle(post.title);

    if (slugSet.has(post.slug)) return;
    if (aliasSet.has(post.slug)) return;
    if (titleSet.has(normalizedTitle)) return;

    merged.push(post);
    slugSet.add(post.slug);
    titleSet.add(normalizedTitle);

    if (post.sourceSlug) {
      aliasSet.add(post.sourceSlug);
    }
  };

  [...baseBlogPosts, ...generatedBlogPosts].forEach(tryAddPost);

  return merged.sort((a, b) => parseDate(b.date) - parseDate(a.date));
};

export const blogPosts: BlogPost[] = buildBlogPosts();

export const getPublishedPosts = (): BlogPost[] => {
  return blogPosts.filter(post => post.published);
};

export const getPostBySlug = (slug: string): BlogPost | undefined => {
  return blogPosts.find(post => post.slug === slug);
};

export const getPostsByCategory = (category: string): BlogPost[] => {
  return blogPosts.filter(post => post.category === category && post.published);
};

export const getPostsByTag = (tag: string): BlogPost[] => {
  return blogPosts.filter(post => post.tags.includes(tag) && post.published);
};

export const getRelatedPosts = (currentPost: BlogPost, limit: number = 3): BlogPost[] => {
  const relatedCandidates = blogPosts.filter(post => {
    if (!post.published) return false;
    if (post.slug === currentPost.slug) return false;

    const sharesCategory = post.category === currentPost.category;
    const sharesTag = post.tags.some(tag => currentPost.tags.includes(tag));
    return sharesCategory || sharesTag;
  });

  const uniqueBySlug: BlogPost[] = [];
  const seen = new Set<string>();

  relatedCandidates.forEach(post => {
    if (!seen.has(post.slug)) {
      seen.add(post.slug);
      uniqueBySlug.push(post);
    }
  });

  return uniqueBySlug.slice(0, limit);
};

export const getAllCategories = (): string[] => {
  const categories = new Set(blogPosts.map(post => post.category));
  return Array.from(categories);
};

export const getAllTags = (): string[] => {
  const tags = new Set(blogPosts.flatMap(post => post.tags));
  return Array.from(tags);
};
